[GenerateTypedNameReferences]
public sealed partial class DoorPurchaseConsoleWindow : DefaultWindow
{
    public event Action<NetEntity>? OnDoorSelected;

    public NavMapControl Map => NavMap; // Expose NavMap for BUI

    private List<(NetEntity Uid, Vector2 Position, string Code)> _currentProperties = new();

    public DoorPurchaseConsoleWindow()
    {
        RobustXamlLoader.Load(this);

        // Listen for clicks on the map area
        NavMap.OnKeyBindUp += OnMapKeyBindUp;
    }

    private void OnMapKeyBindUp(GUIBoundKeyEventArgs args)
    {
        if (args.Function != EngineKeyFunctions.UIClick)
            return;

        // Convert interaction position to local coords relative to NavMap
        // But NavMap handles zooming/panning.
        // We should check if we clicked near a marker.

        // NavMap doesn't expose WorldToLocal directly publicly in a way that includes zoom/pan easily
        // UNLESS we use its parsing logic.
        // Actually, NavMapControl tracks Coordinate systems.

        // Workaround: We know the markers are at WorldCoords.
        // We can ask NavMap to tell us screen coords of valid markers.
        // Or we assume 1:1 if unzoomed? No, zoom is possible.

        // Let's use the mouse position in screen space AND transform our markers to screen space.
        // NavMap inherits Control.

        foreach (var prop in _currentProperties)
        {
            // This is tricky without access to NavMap internal transform.
            // But NavMapControl usually has a "TrackedCoordinates" dictionary.
            // If we add them to TrackedCoordinates, NavMap renders them.
            // Does NavMap expose a way to "Get Entity Under Mouse"?
            // No standard API in SS14 for that specific control usually.

            // Alternative: NavMapControl usually exposes the drawing matrix or we can assume simple click.
            // Wait, NavMapControl HAS "TrackedEntitySelectedAction".
            // If we use `TrackedCoordinates` with EntityCoordinates, maybe clicking works?
            // "TrackedEntitySelectedAction" invokes with EntityCoordinates?
            // Let's check NavMapControl.cs in my memory/previous reads.
            // KeyBindUp in NavMapControl invokes `TrackedEntitySelectedAction` with the closest entity!
            // THAT IS PERFECT!
        }
    }

    public void UpdateState(DoorPurchaseConsoleState state, IEntityManager entManager)
    {
        if (state.MapEntity == null) return;

        var mapUid = entManager.GetEntity(state.MapEntity.Value);
        NavMap.MapUid = mapUid;

        // Clear old
        NavMap.TrackedCoordinates.Clear();
        NavMap.CustomBeacons.Clear();
        NavMap.TrackedEntities.Clear();
        _currentProperties.Clear();

        var cache = IoCManager.Resolve<IResourceCache>();
        var texture = cache.GetResource<TextureResource>("/Textures/Interface/NavMap/beveled_circle.png").Texture;

        foreach (var prop in state.Properties)
        {
            var entityUid = entManager.GetEntity(prop.Uid);
            var coordinates = entManager.GetCoordinates(prop.Coordinates);

            // Add to visualization
            // We use CustomBeacons for text (DoorCode)
            NavMap.CustomBeacons.Add((coordinates, prop.DoorCode, Color.Yellow, false));

            // Add to Clickable Entities
            // We use the ID as the key
            var blip = new NavMapBlip(coordinates, texture, Color.Yellow, false, true, 1.5f);
            NavMap.TrackedEntities[prop.Uid] = blip;

            // Store for lookup if needed (though NavMap gives us coords back)
            _currentProperties.Add((prop.Uid, coordinates.Position, prop.DoorCode));
        }

        NavMap.ForceNavMapUpdate();
    }
}
