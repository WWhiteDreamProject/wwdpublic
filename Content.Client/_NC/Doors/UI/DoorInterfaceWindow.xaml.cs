using Content.Shared._NC.Doors.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using System;

namespace Content.Client._NC.Doors.UI;

[GenerateTypedNameReferences]
public sealed partial class DoorInterfaceWindow : DefaultWindow
{
    public event Action? OnAction;
    public event Action? OnLockAction;
    private bool _isOwner;
    private bool _hasOwner;

    public DoorInterfaceWindow()
    {
        RobustXamlLoader.Load(this);

        ActionBtn.OnPressed += _ => OnAction?.Invoke();
        LockBtn.OnPressed += _ => OnLockAction?.Invoke();
    }

    public void UpdateState(DoorInterfaceState state, bool isOwner)
    {
        _isOwner = isOwner;
        _hasOwner = state.OwnerName != null;

        AddressLabel.Text = state.Address;
        OwnerLabel.Text = state.OwnerName ?? "Нет";
        PriceLabel.Text = $"Цена: {state.Price}$";

        if (!_hasOwner)
        {
            ActionBtn.Text = "Купить";
            ActionBtn.Disabled = false;

            LockBtn.Disabled = true;
            LockBtn.Text = "Закрыть";
        }
        else if (_isOwner)
        {
            ActionBtn.Text = "Продать";
            ActionBtn.Disabled = false;

            LockBtn.Disabled = false;
            LockBtn.Text = state.IsLocked ? "Открыть" : "Закрыть";
        }
        else
        {
            ActionBtn.Text = "Продано";
            ActionBtn.Disabled = true;

            LockBtn.Disabled = true;
            LockBtn.Text = state.IsLocked ? "Закрыто" : "Открыто";
        }
    }

    public bool IsBuyAction => !_hasOwner;
}
