using Content.Client.UserInterface.Controls;
using Content.Shared._NC.Ncpd;
using Content.Shared._NC.CitiNet;
using Robust.Client.AutoGenerated;
using Robust.Client.Player;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface.Controls;
using Robust.Shared.Timing;
using Robust.Shared.Utility;

namespace Content.Client._NC.Ncpd
{
    [GenerateTypedNameReferences]
    public sealed partial class NcpdTabletWindow : FancyWindow
    {
        public event Action<int>? OnCallSelected;
        public event Action<int>? OnCallCleared;

        public NcpdTabletWindow()
        {
            RobustXamlLoader.Load(this);

            ClearCallBtn.OnPressed += _ =>
            {
                if (_selectedCallId.HasValue)
                    OnCallCleared?.Invoke(_selectedCallId.Value);
            };
        }

        private int? _selectedCallId;

        public void UpdateState(NcpdTabletState state)
        {
            _selectedCallId = state.SelectedCallId;
            
            // Update Map Grid
            var entManager = IoCManager.Resolve<IEntityManager>();
            if (state.GridUid != null)
                NavMap.MapUid = entManager.GetEntity(state.GridUid.Value);

            // Update Map data
            NavMap.MapSectors = state.Sectors;
            NavMap.MapBeacons = new List<CitiNetMapBeaconData>(state.Beacons);
            NavMap.MapPings = state.Pings;

            // Add SELF marker
            var player = IoCManager.Resolve<Robust.Client.Player.IPlayerManager>().LocalSession?.AttachedEntity;
            if (player != null)
            {
                var xform = entManager.GetComponent<TransformComponent>(player.Value);
                var playerPos = xform.LocalPosition;
                NavMap.MapBeacons.Add(new CitiNetMapBeaconData(
                    entManager.GetNetEntity(player.Value),
                    "YOU",
                    null,
                    Color.FromHex("#00E5FF"),
                    playerPos,
                    10,
                    false,
                    true // IsSelf = true
                ));
            }

            // Update Call List
            CallListContainer.DisposeAllChildren();
            foreach (var call in state.ActiveCalls)
            {
                var btn = new Button
                {
                    Text = $"#{call.Id} - {call.Title}",
                    HorizontalExpand = true,
                    Margin = new Thickness(0, 0, 0, 5),
                    ToggleMode = true,
                    Pressed = call.Id == _selectedCallId
                };

                btn.OnPressed += _ => OnCallSelected?.Invoke(call.Id);
                CallListContainer.AddChild(btn);
            }

            // Update Details
            var selectedCall = state.ActiveCalls.Find(c => c.Id == _selectedCallId);
            if (selectedCall.Id != 0)
            {
                CallDetailsContainer.Visible = true;
                NoCallContainer.Visible = false;
                CallTitleLabel.Text = selectedCall.Title;
                CallSectorLabel.Text = selectedCall.Sector;
                CallDescriptionLabel.Text = selectedCall.Description;

                // Update Map position
                var coords = entManager.GetCoordinates(selectedCall.Coordinates);
                
                // Add Personal Case Marker
                NavMap.MapBeacons.Add(new CitiNetMapBeaconData(
                    default,
                    $"[!] {selectedCall.Title}",
                    new SpriteSpecifier.Texture(new ResPath("/Textures/Interface/NavMap/beveled_star.png")),
                    Color.FromHex("#FF5252"),
                    coords.Position,
                    10
                ));

                NavMap.CenterToCoordinates(coords);
                NavMap.ForceNavMapUpdate();
            }
            else
            {
                CallDetailsContainer.Visible = false;
                NoCallContainer.Visible = true;
            }
        }
    }
}
