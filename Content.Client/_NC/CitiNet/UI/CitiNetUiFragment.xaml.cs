    // Current tab index (0=Direct, 1=Chats, 2=DataPool, 3=Status, 4=Live)
    private int _currentTab = 1; // Default to Chat Rooms

    // Cache the state so we can rebuild the UI when switching tabs
    private CitiNetUiState? _lastState;
    private EntityUid? _currentCamera;

