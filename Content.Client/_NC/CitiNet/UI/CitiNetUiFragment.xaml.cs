    protected override void Dispose(bool disposing)
    {
        base.Dispose(disposing);

        if (disposing && _currentCamera != null)
        {
            if (IoCManager.Resolve<IEntityManager>().TrySystem<Content.Client.Eye.EyeLerpingSystem>(out var eyeSystem))
            {
                eyeSystem.RemoveEye(_currentCamera.Value);
            }
            _currentCamera = null;
        }
    }
