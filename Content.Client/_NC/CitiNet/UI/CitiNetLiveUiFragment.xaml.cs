// NC — Код-бехинд для CitiNet Live UI фрагмента.
// Три экрана: список стримов, стример, зритель.

using Content.Client.Message;
using Content.Client.Resources;
using Content.Shared._NC.CitiNet.Live;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Graphics;
using Robust.Shared.Maths;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;

namespace Content.Client._NC.CitiNet.UI;

[GenerateTypedNameReferences]
public sealed partial class CitiNetLiveUiFragment : BoxContainer
{
    // Цвета в стиле кибер-терминала
    private static readonly Color ColorCyan = Color.FromHex("#42e3f5");
    private static readonly Color ColorLime = Color.FromHex("#7dfc5b");
    private static readonly Color ColorYellow = Color.FromHex("#f5d442");
    private static readonly Color ColorRed = Color.FromHex("#f54242");
    private static readonly Color ColorDimGreen = Color.FromHex("#3a7a3a");

    // Eye по умолчанию (пустой) для вьюпорта
    private readonly IEye _defaultEye = new Robust.Shared.Graphics.Eye();

    // Состояние панели доната
    private bool _donatePanelVisible;

    /// <summary>Делегат для отправки Live-сообщений на сервер.</summary>
    public event Action<CitiNetLiveMessageType, string?>? OnSendLiveMessage;

    public CitiNetLiveUiFragment()
    {
        RobustXamlLoader.Load(this);

        // Инициализация вьюпорта камеры
        try
        {
            var resourceCache = IoCManager.Resolve<IResourceCache>();
            var protoMan = IoCManager.Resolve<IPrototypeManager>();

            var shader = protoMan.Index<ShaderPrototype>("CameraStatic").Instance().Duplicate();
            var texture = resourceCache.GetTexture("/Textures/Interface/Nano/square_black.png");

            CameraView.ViewportSize = new Vector2i(600, 600);
            CameraView.Eye = _defaultEye;
            CameraViewBackground.Stretch = TextureRect.StretchMode.Scale;
            CameraViewBackground.Texture = texture;
            CameraViewBackground.ShaderOverride = shader;
        }
        catch { }

        // Заголовок
        HeaderLabel.SetMarkup($"[color={ColorYellow.ToHex()}][bold]CITINET LIVE v2.0[/bold][/color]");

        StartStreamBtn.OnPressed += _ =>
        {
            var title = StreamTitleInput.Text?.Trim();
            OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.StartStream, string.IsNullOrWhiteSpace(title) ? "live" : title);
            StreamTitleInput.Text = string.Empty;
        };
        StopStreamBtn.OnPressed += _ => OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.StopStream, null);
        BackToListBtn.OnPressed += _ => OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.StopWatching, null);

        // Чат стримера
        StreamerChatSendBtn.OnPressed += _ =>
        {
            var text = StreamerChatInput.Text.Trim();
            if (!string.IsNullOrEmpty(text))
            {
                OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.SendChat, text);
                StreamerChatInput.Text = string.Empty;
            }
        };

        // Чат зрителя
        ViewerSendBtn.OnPressed += _ =>
        {
            var text = ViewerChatInput.Text.Trim();
            if (!string.IsNullOrEmpty(text))
            {
                OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.SendChat, text);
                ViewerChatInput.Text = string.Empty;
            }
        };

        // Переключение панели доната
        ViewerDonateBtn.OnPressed += _ =>
        {
            _donatePanelVisible = !_donatePanelVisible;
            DonatePanel.Visible = _donatePanelVisible;
        };

        // Отправка доната
        DonateSubmitBtn.OnPressed += _ =>
        {
            var amountStr = DonateAmountInput.Text.Trim();
            var msgText = DonateMessageInput.Text.Trim();
            if (!int.TryParse(amountStr, out var amount) || amount <= 0)
                return;

            var finalMsg = string.IsNullOrEmpty(msgText) ? " " : msgText;
            OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.SendDonate, $"{amount}|{finalMsg}");
            DonateAmountInput.Text = string.Empty;
            DonateMessageInput.Text = string.Empty;
            _donatePanelVisible = false;
            DonatePanel.Visible = false;
        };
    }

    // ===================== UPDATE STATE =====================

    public void UpdateState(CitiNetLiveUiState state)
    {
        // Определяем какой экран показать
        if (state.WatchedCamNetEntity.HasValue)
        {
            // Экран 3: Зритель
            ShowViewerScreen(state);
        }
        else if (state.IsStreaming)
        {
            // Экран 2: Стример
            ShowStreamerScreen(state);
        }
        else
        {
            // Экран 1: Список стримов
            ShowStreamListScreen(state);
        }
    }

    // ===================== ЭКРАН 1: СПИСОК =====================

    private void ShowStreamListScreen(CitiNetLiveUiState state)
    {
        StreamListPanel.Visible = true;
        StreamerPanel.Visible = false;
        ViewerPanel.Visible = false;

        // Счётчик
        StreamCountLabel.SetMarkup($"[color={ColorLime.ToHex()}]{state.ActiveStreams.Count}/5 Streams Active[/color]");

        // Очищаем и заполняем список стримов
        StreamListContainer.RemoveAllChildren();

        if (state.ActiveStreams.Count == 0)
        {
            StreamListContainer.AddChild(new RichTextLabel
            {
                Margin = new Thickness(0, 8, 0, 4),
            });
            // Используем SetMarkup после создания
            var noStreamsLabel = new RichTextLabel { Margin = new Thickness(0, 8, 0, 4) };
            noStreamsLabel.SetMarkup($"[color={ColorDimGreen.ToHex()}]No active streams[/color]");
            StreamListContainer.AddChild(noStreamsLabel);
        }
        else
        {
            foreach (var stream in state.ActiveStreams)
            {
                var card = CreateStreamCard(stream);
                StreamListContainer.AddChild(card);
            }
        }

        // Панель старта стрима (заголовок + кнопка) — только если есть камера
        StartStreamPanel.Visible = state.HasCamera;
    }

    private BoxContainer CreateStreamCard(StreamInfo stream)
    {
        var card = new BoxContainer
        {
            Orientation = LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            Margin = new Thickness(0, 2, 0, 2),
            MinHeight = 40,
        };

        // Инфо
        var info = new BoxContainer
        {
            Orientation = LayoutOrientation.Vertical,
            HorizontalExpand = true,
        };

        var titleLabel = new RichTextLabel();
        titleLabel.SetMarkup($"[color={ColorYellow.ToHex()}]#{FormattedMessage.EscapeText(stream.Title)}[/color]");
        info.AddChild(titleLabel);

        var viewersLabel = new RichTextLabel();
        viewersLabel.SetMarkup($"[color={ColorLime.ToHex()}]👁 {stream.ViewerCount}[/color]");
        info.AddChild(viewersLabel);

        card.AddChild(info);

        // Кнопка WATCH
        var watchBtn = new Button
        {
            Text = "[WATCH]",
            MinWidth = 70,
        };
        var camNet = stream.CamNetEntity;
        watchBtn.OnPressed += _ => OnSendLiveMessage?.Invoke(CitiNetLiveMessageType.WatchStream, camNet.ToString());
        card.AddChild(watchBtn);

        return card;
    }

    // ===================== ЭКРАН 2: СТРИМЕР =====================

    private void ShowStreamerScreen(CitiNetLiveUiState state)
    {
        StreamListPanel.Visible = false;
        StreamerPanel.Visible = true;
        ViewerPanel.Visible = false;

        // Шапка
        StreamerLiveLabel.SetMarkup($"[color={ColorRed.ToHex()}][bold]● LIVE[/bold][/color]");
        StreamerViewerLabel.SetMarkup($"[color={ColorLime.ToHex()}]👁 {state.ViewerCount}[/color]");
        StreamerBatteryLabel.SetMarkup($"[color={ColorYellow.ToHex()}]🔋 {state.BatteryPercent}%[/color]");

        // Чат
        RenderChatMessages(StreamerChatContainer, state.ChatMessages);
    }

    // ===================== ЭКРАН 3: ЗРИТЕЛЬ =====================

    private void ShowViewerScreen(CitiNetLiveUiState state)
    {
        StreamListPanel.Visible = false;
        StreamerPanel.Visible = false;
        ViewerPanel.Visible = true;

        // ● LIVE + имя стримера
        ViewerLiveLabel.SetMarkup($"[color={ColorRed.ToHex()}][bold]● LIVE[/bold][/color]");
        ViewerStreamerLabel.SetMarkup($"[color={ColorCyan.ToHex()}]{FormattedMessage.EscapeText(state.WatchedStreamerName)}[/color]");

        // Чат
        RenderChatMessages(ViewerChatContainer, state.ChatMessages);
    }

    // ===================== ОБЩИЕ =====================

    private void RenderChatMessages(BoxContainer container, List<LiveChatMessage> messages)
    {
        container.RemoveAllChildren();

        foreach (var msg in messages)
        {
            var escaped = FormattedMessage.EscapeText(msg.Text);
            var sender = FormattedMessage.EscapeText(msg.Sender);
            var color = msg.IsSystem ? ColorRed.ToHex() : ColorCyan.ToHex();

            var label = new RichTextLabel { HorizontalExpand = true };
            label.SetMarkup($"[color={color}]{sender}:[/color] {escaped}");
            container.AddChild(label);
        }
    }

    /// <summary>Установить или сбросить Eye для вьюпорта камеры.</summary>
    public void SetCameraView(IEye? eye)
    {
        if (eye != null)
        {
            CameraView.Eye = eye;
            CameraView.Visible = true;
            CameraViewBackground.Visible = false;
        }
        else
        {
            CameraView.Eye = _defaultEye;
            CameraView.Visible = false;
            CameraViewBackground.Visible = true;
        }
    }
}
