using System;
using System.Collections.Generic;
using System.Linq;
using Content.Shared._NC.Dispatch;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;
using System.Numerics;
using Robust.Client.Graphics;
using Robust.Shared.Utility;

namespace Content.Client._NC.Dispatch
{
    public sealed partial class OverwatchConsoleWindow : Content.Client.UserInterface.Controls.FancyWindow
    {
        public event Action<int, OverwatchAlertAction>? OnAlertAction;

        private readonly BoxContainer _alertList;

        private sealed class AlertRow
        {
            public PanelContainer Panel = default!;
            public Label TimeLabel = default!;
            public Label TypeLabel = default!;
            public Label SectorLabel = default!;
            public Label CameraLabel = default!;
            public Button ConnectBtn = default!;
            public Button PrintBtn = default!;
            public Button ArchiveBtn = default!;
            public int AlertId;
        }

        private readonly Dictionary<int, AlertRow> _rows = new();

        public OverwatchConsoleWindow()
        {
            RobustXamlLoader.Load(this);
            _alertList = FindControl<BoxContainer>("AlertList");
        }

        public void UpdateState(OverwatchConsoleState state)
        {
            var currentIds = new HashSet<int>(state.Alerts.Select(a => a.Id));
            var toRemove = _rows.Keys.Where(k => !currentIds.Contains(k)).ToList();
            foreach (var id in toRemove)
            {
                _rows[id].Panel.Orphan();
                _rows.Remove(id);
            }

            foreach (var alert in state.Alerts)
            {
                if (!_rows.TryGetValue(alert.Id, out var row))
                {
                    row = CreateAlertRow(alert.Id);
                    _rows[alert.Id] = row;
                }

                UpdateAlertRow(row, alert);
            }
        }

        private AlertRow CreateAlertRow(int id)
        {
            var row = new AlertRow { AlertId = id };
            row.Panel = new PanelContainer { MinHeight = 35 };
            row.Panel.PanelOverride = new StyleBoxFlat { BackgroundColor = Color.FromHex("#0b171c") };

            var content = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                Margin = new Thickness(10, 5),
                HorizontalExpand = true
            };

            row.TimeLabel = new Label { MinWidth = 60, FontColorOverride = Color.FromHex("#EEEEEE") };
            content.AddChild(row.TimeLabel);
            content.AddChild(new Control { MinSize = new Vector2(10, 0) });

            row.TypeLabel = new Label { MinWidth = 80, FontColorOverride = Color.FromHex("#FF5252") };
            content.AddChild(row.TypeLabel);
            content.AddChild(new Control { MinSize = new Vector2(10, 0) });

            row.SectorLabel = new Label { HorizontalExpand = true, ClipText = true, FontColorOverride = Color.FromHex("#CCCCCC") };
            content.AddChild(row.SectorLabel);
            content.AddChild(new Control { MinSize = new Vector2(10, 0) });

            row.CameraLabel = new Label { MinWidth = 120, FontColorOverride = Color.FromHex("#CCCCCC") };
            content.AddChild(row.CameraLabel);
            content.AddChild(new Control { MinSize = new Vector2(10, 0) });

            // actions
            var actions = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, SeparationOverride = 2 };
            row.ConnectBtn = new Button { Text = "Смотреть", MinWidth = 70 };
            row.PrintBtn = new Button { Text = "Печать", MinWidth = 60 };
            row.ArchiveBtn = new Button { Text = "Архив", MinWidth = 60 };
            var dispatchBtn = new Button { Text = "В Планшет", MinWidth = 80, Modulate = Color.FromHex("#4DD0E1") };

            row.ConnectBtn.OnPressed += _ => OnAlertAction?.Invoke(id, OverwatchAlertAction.ConnectCamera);
            row.PrintBtn.OnPressed += _ => OnAlertAction?.Invoke(id, OverwatchAlertAction.PrintTicket);
            row.ArchiveBtn.OnPressed += _ => OnAlertAction?.Invoke(id, OverwatchAlertAction.Archive);
            dispatchBtn.OnPressed += _ => OnAlertAction?.Invoke(id, OverwatchAlertAction.DispatchToTablet);

            actions.AddChild(row.ConnectBtn);
            actions.AddChild(dispatchBtn);
            actions.AddChild(row.PrintBtn);
            actions.AddChild(row.ArchiveBtn);

            content.AddChild(actions);

            row.Panel.AddChild(content);
            return row;
        }

        private void UpdateAlertRow(AlertRow row, OverwatchAlertData alert)
        {
            row.TimeLabel.Text = alert.TimeStr;
            row.TypeLabel.Text = alert.Type;
            row.SectorLabel.Text = alert.Sector;
            row.CameraLabel.Text = alert.CameraName;

            if (row.Panel.Parent != _alertList)
            {
                row.Panel.Orphan();
                _alertList.AddChild(row.Panel);
            }
        }
    }
}