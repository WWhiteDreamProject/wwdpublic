using System.Numerics; // For Vector2
using Content.Shared._NC.Trauma;
using Content.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls; // For BoxContainer, Label, etc.
using Robust.Client.UserInterface; // For Control
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths; // Для Thickness

namespace Content.Client._NC.Trauma
{
    public sealed partial class TraumaComputerWindow : FancyWindow
    {
        public event Action<NetEntity, TraumaSubscriptionTier>? OnSubscriptionChanged;
        public event Action<NetEntity>? OnDispatch;

        private readonly BoxContainer _platinumList;
        private readonly BoxContainer _generalList;
        private readonly BoxContainer _logList;

        public TraumaComputerWindow()
        {
            RobustXamlLoader.Load(this);
            _platinumList = FindControl<BoxContainer>("PlatinumList");
            _generalList = FindControl<BoxContainer>("GeneralList");
            _logList = FindControl<BoxContainer>("LogList");
        }

        public void UpdateState(TraumaComputerState state)
        {
            _platinumList.DisposeAllChildren();
            _generalList.DisposeAllChildren();

            foreach (var patient in state.Patients)
            {
                var rowPanel = new PanelContainer
                {
                    StyleClasses = { "LowDivider" }, // Generic panel style
                    MinHeight = 45
                    // Modulate remove (default dark)
                };

                var rowContent = new BoxContainer
                {
                    Orientation = BoxContainer.LayoutOrientation.Horizontal,
                    Margin = new Thickness(10, 5),
                    HorizontalExpand = true
                };

                // Name - White/Light Grey
                rowContent.AddChild(new Label
                {
                    Text = patient.Name,
                    HorizontalExpand = true,
                    ClipText = true,
                    StyleClasses = { "LabelHeading" },
                    FontColorOverride = Color.FromHex("#EEEEEE") // Grey 200
                });

                rowContent.AddChild(new Control { MinSize = new Vector2(10, 0) });

                // Job - Grey 400
                rowContent.AddChild(new Label
                {
                    Text = patient.Job ?? "Unknown",
                    MinWidth = 100,
                    ClipText = true,
                    FontColorOverride = Color.FromHex("#BDBDBD") // Grey 400
                });

                rowContent.AddChild(new Control { MinSize = new Vector2(10, 0) });

                // Damage - Red 400 (Lighter for dark bg)
                var dmgText = (patient.Subscription == TraumaSubscriptionTier.Platinum)
                              ? (patient.DamageInfo ?? "N/A")
                              : "";

                rowContent.AddChild(new Label
                {
                    Text = dmgText,
                    MinWidth = 80,
                    FontColorOverride = Color.FromHex("#EF5350") // Red 400
                });

                rowContent.AddChild(new Control { MinSize = new Vector2(10, 0) });

                // Status Label
                var statusColor = patient.HealthStatus switch
                {
                    "Dead" => Color.FromHex("#D32F2F"), // Red 700
                    "Critical" => Color.FromHex("#F57C00"), // Orange 700
                    _ => Color.FromHex("#388E3C") // Green 700
                };

                rowContent.AddChild(new Label
                {
                    Text = patient.HealthStatus,
                    MinWidth = 100,
                    FontColorOverride = statusColor,
                    Align = Label.AlignMode.Center
                });

                rowContent.AddChild(new Control { MinSize = new Vector2(20, 0) });

                // Subscription Dropdown
                var tierSelector = new OptionButton { MinWidth = 140 };

                for (int i = 0; i < 4; i++)
                {
                    var tier = (TraumaSubscriptionTier) i;
                    tierSelector.AddItem(tier.ToString(), i);

                    if (tier == patient.Subscription)
                        tierSelector.Select(i);
                }

                tierSelector.OnItemSelected += args =>
                {
                    tierSelector.SelectId(args.Id);
                    var newTier = (TraumaSubscriptionTier) args.Id;
                    OnSubscriptionChanged?.Invoke(patient.EntityUid, newTier);
                };

                rowContent.AddChild(tierSelector);

                rowContent.AddChild(new Control { MinSize = new Vector2(10, 0) });

                if (patient.Subscription == TraumaSubscriptionTier.Platinum)
                {
                    var dispatchBtn = new Button
                    {
                        Text = "DISPATCH",
                        Modulate = Color.Red,
                        MinWidth = 80
                    };
                    dispatchBtn.OnPressed += _ => OnDispatch?.Invoke(patient.EntityUid);
                    rowContent.AddChild(dispatchBtn);
                }


                rowPanel.AddChild(rowContent);

                // Sort into lists
                if (patient.Subscription == TraumaSubscriptionTier.Platinum)
                    _platinumList.AddChild(rowPanel);
                else
                    _generalList.AddChild(rowPanel);
            }

            // Update Logs
            _logList.DisposeAllChildren();
            // Show newest first
            for (int i = state.Logs.Count - 1; i >= 0; i--)
            {
                var log = state.Logs[i];
                var timeStr = log.Time.ToString(@"hh\:mm\:ss");

                var logLabel = new Label
                {
                    Text = $"[{timeStr}] {log.Editor} changed {log.Target}: {log.OldTier} -> {log.NewTier}",
                    FontColorOverride = Color.LightGray
                };

                _logList.AddChild(logLabel);
            }
        }
    }
}
