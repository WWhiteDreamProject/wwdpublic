using System.Numerics;
using Content.Shared._NC.Trauma;
using Content.Client.UserInterface.Controls;
using System.Linq;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.GameObjects;

namespace Content.Client._NC.Trauma
{
    public sealed partial class TraumaTabletWindow : FancyWindow
    {
        public event Action<NetEntity>? OnOpenMap; // Entity ID to center on?

        private NetEntity? _currentPatient;

        // Controls
        private Button OpenMapButton;
        private BoxContainer PatientDetailContainer; // Used for "Has Patient" view
        private BoxContainer EmptyContainer; // Used for "No Patient" view
        private Label TitleLabel;
        private Label PatientNameLabel;
        private Label PatientJobLabel;
        private Label PatientStatusLabel;
        private Label PatientDamageLabel;
        private BoxContainer DamageList;

        public TraumaTabletWindow()
        {
            RobustXamlLoader.Load(this);

            OpenMapButton = FindControl<Button>("OpenMapButton");
            PatientDetailContainer = FindControl<BoxContainer>("PatientDetailContainer");
            EmptyContainer = FindControl<BoxContainer>("PatientListContainer"); // reusing container or renaming in XAML? Reusing for now as "Empty View" carrier
            TitleLabel = FindControl<Label>("TitleLabel");
            PatientNameLabel = FindControl<Label>("PatientNameLabel");
            PatientJobLabel = FindControl<Label>("PatientJobLabel");
            PatientStatusLabel = FindControl<Label>("PatientStatusLabel");
            PatientDamageLabel = FindControl<Label>("PatientDamageLabel");
            DamageList = FindControl<BoxContainer>("DamageList");

            OpenMapButton.OnPressed += _ => OnOpenMap?.Invoke(_currentPatient ?? default);

            // Initial state: Empty
            ShowEmpty();
        }

        public void UpdateState(TraumaPatientData? patient)
        {
            if (patient.HasValue)
            {
                ShowDetails(patient.Value);
            }
            else
            {
                ShowEmpty();
            }
        }

        private void ShowDetails(TraumaPatientData patient)
        {
            _currentPatient = patient.EntityUid;

            // Populate view
            PopulateDetails(patient);

            // Switch view
            EmptyContainer.Visible = false;
            PatientDetailContainer.Visible = true;
            TitleLabel.Text = "ACTIVE DISPATCH";
            OpenMapButton.Visible = true;
        }

        private void PopulateDetails(TraumaPatientData patient)
        {
            PatientNameLabel.Text = patient.Name;
            PatientJobLabel.Text = patient.Job ?? "Unknown";
            PatientStatusLabel.Text = patient.HealthStatus;

            // Color status
            PatientStatusLabel.FontColorOverride = patient.HealthStatus switch
            {
                "Dead" => Color.Red,
                "Critical" => Color.Orange,
                _ => Color.Green
            };

            PatientDamageLabel.Text = patient.DamageInfo;

            DamageList.DisposeAllChildren();
            DamageList.AddChild(new Label { Text = patient.DamageInfo });
        }

        private void ShowEmpty()
        {
            _currentPatient = null;
            EmptyContainer.Visible = true;
            PatientDetailContainer.Visible = false;
            // BackButton not used
            TitleLabel.Text = "NO ACTIVE DISPATCH";
            OpenMapButton.Visible = false; // Maybe allow map even without patient? User said "Open Map UI won't open", implying button should be there.
            // Requirement: "Open Map ЮИ карты не открывает".
            // If I hide button, they can't open map.
            // Let's keep button visible? Or only on dispatch?
            // User requested "Tablet should be empty, patient appears only after dispatch".
            // Map button is separate feature. Probably keep it available?
            OpenMapButton.Visible = true;

            // Clear EmptyContainer content and show text?
            EmptyContainer.DisposeAllChildren();
            EmptyContainer.AddChild(new Label
            {
                Text = "Waiting for dispatch...",
                HorizontalAlignment = HAlignment.Center,
                VerticalAlignment = VAlignment.Center,
                Margin = new Thickness(0, 50, 0, 0),
                FontColorOverride = Color.Gray
            });
        }
    }
}
