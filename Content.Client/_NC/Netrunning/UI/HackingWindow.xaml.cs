using Content.Client.Message;
using Content.Shared._NC.Netrunning.UI;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface.Controls;
using Robust.Shared.Prototypes;

namespace Content.Client._NC.Netrunning.UI;

[GenerateTypedNameReferences]
public sealed partial class HackingWindow : DefaultWindow
{
    public event Action<NetEntity>? OnUseProgram;
    public event Action<string>? OnSubmitPassword;

    private readonly Label _targetName;
    private readonly Label _heatLabel;
    private readonly Label _floorLabel;
    private readonly Label _timerLabel;
    private readonly Label _iceTypeLabel;
    private readonly ProgressBar _iceHealthBar;
    private readonly Label _iceHealthLabel;
    private readonly ProgressBar _playerRamBar;
    private readonly Label _playerRamLabel;
    private readonly GridContainer _programGrid;
    private readonly BoxContainer _passwordContainer;
    private readonly LineEdit _passwordInput;
    private readonly Button _submitPasswordButton;

    public HackingWindow()
    {
        RobustXamlLoader.Load(this);

        _targetName = FindControl<Label>("TargetName");
        _heatLabel = FindControl<Label>("HeatLabel");
        _floorLabel = FindControl<Label>("FloorLabel");
        _timerLabel = FindControl<Label>("TimerLabel");
        _iceTypeLabel = FindControl<Label>("IceTypeLabel");
        _iceHealthBar = FindControl<ProgressBar>("IceHealthBar");
        _iceHealthLabel = FindControl<Label>("IceHealthLabel");
        _playerRamBar = FindControl<ProgressBar>("PlayerRamBar");
        _playerRamLabel = FindControl<Label>("PlayerRamLabel");
        _programGrid = FindControl<GridContainer>("ProgramGrid");
        _passwordContainer = FindControl<BoxContainer>("PasswordContainer");
        _passwordInput = FindControl<LineEdit>("PasswordInput");
        _submitPasswordButton = FindControl<Button>("SubmitPasswordButton");

        _submitPasswordButton.OnPressed += _ =>
        {
            OnSubmitPassword?.Invoke(_passwordInput.Text);
            _passwordInput.Text = "";
        };
    }

    public void UpdateState(HackingBoundUiState state)
    {
        _targetName.Text = state.TargetSlot; // Or Server Name?
        _floorLabel.Text = state.TargetSlot;

        // Heat
        _heatLabel.Text = $"{state.AccumulatedDamage}";

        // Update timer
        _timerLabel.Text = $"{(int) state.RemainingTime}";
        if (state.RemainingTime <= 3)
            _timerLabel.FontColorOverride = Color.Red;
        else
            _timerLabel.FontColorOverride = Color.FromHex("#FF5500");

        // Update ICE
        _iceTypeLabel.Text = $"{state.TargetIceType} ЛЁД ОБНАРУЖЕН";
        _iceHealthBar.MaxValue = state.TargetIceMaxHealth > 0 ? state.TargetIceMaxHealth : 1;
        _iceHealthBar.Value = state.TargetIceHealth;
        _iceHealthLabel.Text = $"{state.TargetIceHealth}/{state.TargetIceMaxHealth}";

        // Update Player RAM
        _playerRamBar.MaxValue = state.PlayerMaxRam > 0 ? state.PlayerMaxRam : 1;
        _playerRamBar.Value = state.PlayerRam;
        _playerRamLabel.Text = $"{state.PlayerRam}/{state.PlayerMaxRam}";

        // Update Programs
        _programGrid.RemoveAllChildren();
        foreach (var program in state.AvailablePrograms)
        {
            var btn = new Button
            {
                Text = $"{program.Name}\n({program.RamCost} RAM)",
                ToolTip = $"Урон: {program.Damage} | Защита: {program.Defense}",
                MinSize = new System.Numerics.Vector2(100, 40),
                Disabled = state.PlayerRam < program.RamCost
            };

            btn.OnPressed += _ => OnUseProgram?.Invoke(program.Entity);
            _programGrid.AddChild(btn);
        }

        // Gate Logic
        if (state.TargetIceType == "Gate")
        {
            _passwordContainer.Visible = true;
            _iceTypeLabel.FontColorOverride = Color.Yellow;
        }
        else
        {
            _passwordContainer.Visible = false;
            _iceTypeLabel.FontColorOverride = Color.Red;
        }
    }
}
