using Content.Shared._NC.Citation;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface.Controls;
using System;

namespace Content.Client._NC.Citation;

[GenerateTypedNameReferences]
public sealed partial class CitationDeviceWindow : DefaultWindow
{
    public event Action<int, string>? OnSubmit;

    public CitationDeviceWindow()
    {
        RobustXamlLoader.Load(this);

        // Стилизация (грубый темно-серый фон)
        // В XAML не всегда можно задать BackgroundColor напрямую, поэтому используем цвета элементов

        SubmitButton.OnPressed += args =>
        {
            if (int.TryParse(AmountInput.Text, out var amount))
            {
                OnSubmit?.Invoke(amount, ReasonInput.Text);
            }
        };

        AmountInput.OnTextChanged += args =>
        {
            // Фильтруем только цифры
            if (!int.TryParse(AmountInput.Text, out var _))
            {
                AmountInput.Text = string.Empty;
            }
        };
    }

    public void UpdateState(CitationDeviceBuiState state)
    {
        TargetNameLabel.Text = state.TargetName;
        LimitLabel.Text = $"{state.MaxLimit} Эдди";

        if (state.TargetActive)
        {
            StatusLabel.Text = "ОЖИДАНИЕ ОТВЕТА ПОДОЗРЕВАЕМОГО...";
            StatusLabel.FontColorOverride = Robust.Shared.Maths.Color.FromHex("#ffb700");
            SubmitButton.Disabled = true;
            AmountInput.Editable = false;
        }
        else
        {
            StatusLabel.Text = "ОЖИДАНИЕ ВВОДА...";
            StatusLabel.FontColorOverride = Robust.Shared.Maths.Color.FromHex("#aaaaaa");
            SubmitButton.Disabled = false;
            AmountInput.Editable = true;
        }
    }
}