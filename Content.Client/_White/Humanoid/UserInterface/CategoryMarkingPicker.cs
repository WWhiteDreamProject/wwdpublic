using System.Collections.Frozen;
using Content.Shared._White.Humanoid.Markings.Managers;
using Content.Shared._White.Humanoid.Markings.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._White.Humanoid.UserInterface;

[GenerateTypedNameReferences]
public sealed partial class CategoryMarkingPicker : Control
{
    [Dependency] private readonly IEntityManager _entity = default!;
    [Dependency] private readonly IPrototypeManager _prototype = default!;
    [Dependency] private readonly MarkingManager _marking = default!;

    private readonly MarkingsViewModel _model;
    private readonly ProtoId<MarkingCategoryPrototype> _category;
    private readonly ProtoId<MarkingGroupPrototype> _group;
    private readonly SpriteSystem _sprite;

    public bool Empty => MarkingTabs.ChildCount == 0;

    public CategoryMarkingPicker(MarkingsViewModel model, ProtoId<MarkingCategoryPrototype> category, ProtoId<MarkingGroupPrototype> group)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _model = model;
        _category = category;
        _group = group;
        _sprite = _entity.System<SpriteSystem>();

        UpdateMarkings();
    }

    protected override void EnteredTree()
    {
        base.EnteredTree();

        _model.BodyAppearanceDataChanged += UpdateMarkings;
        _model.EnforcementsChanged += UpdateMarkings;
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();

        _model.BodyAppearanceDataChanged -= UpdateMarkings;
        _model.EnforcementsChanged -= UpdateMarkings;
    }

    private void UpdateMarkings()
    {
        if (!_prototype.TryIndex(_category, out var categoryPrototype))
            return;

        if (!_model.AppearanceData.TryGetValue(categoryPrototype.Type, out var appearanceData))
            return;

        MarkingTabs.RemoveAllChildren();
        MarkingTabs.TabsVisible = false;

        FrozenDictionary<string, MarkingPrototype>? markings = null;
        if (_model.EnforceGroupAndSexRestrictions && !_marking.TryGetMarkingsByCategoryAndGroupAndSex(_category, _group, appearanceData.Sex, out markings))
            return;

        if (!_model.EnforceGroupAndSexRestrictions && !_marking.TryGetMarkingsByCategory(_category, out markings))
            return;

        if (markings is null || markings.Count == 0)
            return;

        var control = new SingleMarkingPicker(markings, _model, _category);
        MarkingTabs.AddChild(control);

        MarkingTabs.TabsVisible = true;
    }
}
