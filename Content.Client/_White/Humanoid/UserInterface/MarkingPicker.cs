using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._White.Humanoid.UserInterface;

[GenerateTypedNameReferences]
public sealed partial class MarkingPicker : Control
{
    private MarkingsViewModel? _model;

    public MarkingPicker()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        UpdateMarkings();
    }

    public void SetModel(MarkingsViewModel model)
    {
        _model = model;

        _model.MarkingsDataChanged += UpdateMarkings;
        _model.EnforcementsChanged += UpdateMarkings;
    }

    protected override void EnteredTree()
    {
        base.EnteredTree();

        if (_model is null)
            return;

        _model.MarkingsDataChanged += UpdateMarkings;
        _model.EnforcementsChanged += UpdateMarkings;
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();

        if (_model is null)
            return;

        _model.MarkingsDataChanged -= UpdateMarkings;
        _model.EnforcementsChanged -= UpdateMarkings;
    }

    private void UpdateMarkings()
    {
        if (_model is null)
            return;

        MarkingTabs.RemoveAllChildren();

        var i = 0;
        foreach (var (category, markingsData) in _model.MarkingsData)
        {
            var control = new CategoryMarkingPicker(_model, category, markingsData.Group);
            if (control.Empty)
                continue;

            MarkingTabs.AddChild(control);
            MarkingTabs.SetTabTitle(i, Loc.GetString($"markings-category-{category.Id}"));
            i++;
        }

        MarkingTabs.CurrentTab = 0;
        MarkingTabs.TabsVisible = i > 1;
    }
}
