    private bool IsTimePeriod(double active, double inactive) => _timing.CurTime.TotalSeconds % (active + inactive) <= active;

    protected override void PreRenderChildren(ref ControlRenderArguments args)
    {
        if (CameraScreen.ViewportSize != CameraScreen.PixelSize)
            CameraScreen.ViewportSize = CameraScreen.PixelSize;

        if (_turretEye is not null)
        {
            _turretEye.Rotation = _eye.CurrentEye.Rotation;

            // stolen from MapGridControl for the purpose of visual parity with radar screen control
            var currentScale = _turretEye.Scale.X;
            var diff =  CameraScale - currentScale;
            const float lerpRate = 10f;

            var newScale = currentScale + (float) Math.Clamp(diff, -lerpRate * MathF.Abs(diff) * _timing.FrameTime.TotalSeconds, lerpRate * MathF.Abs(diff) * _timing.FrameTime.TotalSeconds);
            _turretEye.Scale = new Vector2(newScale);
        }

        if (CurrentTurret is null ||
            !_gun.TryGetGun(CurrentTurret.Value, out var gunUid, out var gunComp))
            return;

        UpdateWeaponIDLabel(gunUid);
        UpdateAmmoCountLabel(gunUid);
        UpdateWeaponCooldownLabel(gunComp);
    }

    private void UpdateWeaponIDLabel(EntityUid gunUid)
    {
        if (!_entMan.TryGetComponent<ModularTurretWeaponComponent>(gunUid, out var modComp))
            return;
        var name = modComp.Name;
        if (name is null)
            name = "UNKNOWN";
        // TODO: uncomment after merging the lobby vanity text from EE, it has a funny RANDOM() func for localization
        //else if (Loc.TryGetString(name, out var newName))
        //    name = newName;

        SetWeaponIDLabel($"WEP: {name}", Color.AntiqueWhite);
    }

    private void UpdateWeaponCooldownLabel(GunComponent gunComp)
    {
        if (gunComp.NextFire >= _timing.CurTime)
            SetWeaponCooldownLabel($"{(gunComp.NextFire - _timing.CurTime).TotalSeconds: 0.00}s", Color.Yellow);
        else
            SetWeaponCooldownLabel("RDY", Color.Green);
    }

    private void UpdateAmmoCountLabel(EntityUid gunUid)
    {
        if (!_gun.TryGetBatteryCharges(gunUid, out var shots, out var capacity))
        {
            SetAmmoCountLabel("BAT: N/A", Color.AntiqueWhite);
            return;
        }

        var chargePercent = (float) shots / capacity * 100;
        var len = (int) MathF.Floor(MathF.Log10(capacity) + 1);
        var shotCounter = $"{shots.ToString($"D{len}")}/{capacity}";
        var warn = chargePercent < 25 && IsTimePeriod(0.2, 0.2) ? "WARN" : string.Empty;
        var batteryString = $"BAT:{(chargePercent < 100 ? " " : "")}{chargePercent: 00.00}% ({shotCounter}) {warn}";
        var batteryStringColor = chargePercent switch
        {
            >= 75 => Color.Green,
            >= 50 => Color.Yellow,
            >= 25 => Color.OrangeRed,
            >= 0 => Color.Red,
            _ => Color.Green
        };
        SetAmmoCountLabel(batteryString, batteryStringColor);
    }

