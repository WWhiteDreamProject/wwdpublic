using System.Numerics;
using Content.Client.Computer;
using Content.Client.UserInterface.Controls;
using Content.Shared._White.RemoteControl.BUIStates;
using Content.Shared.Shuttles.BUIStates;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Map;
using Robust.Shared.Timing;

namespace Content.Client._White.RemoteControlConsole.UI;

[GenerateTypedNameReferences]
public sealed partial class RemoteControlConsoleWindow : FancyWindow, IComputerWindow<NavInterfaceState>
{
    private IGameTiming _timing = default!;
    private Font _font = default!;
    public RemoteControlConsoleError Error { get; private set; }

    public RemoteControlConsoleWindow()
    {
        RobustXamlLoader.Load(this);
        _font = new VectorFont(IoCManager.Resolve<IResourceCache>().GetResource<FontResource>("/Fonts/_White/LCD14/LCD14.otf"), 16);
        _timing = IoCManager.Resolve<IGameTiming>();
        RadarScreen.DrawAfterBackground += DrawNoSignal;
    }

    public void SetError(RemoteControlConsoleError error)
    {
        Error = error;
        RadarScreen.ForceSkip = Error != RemoteControlConsoleError.None;
    }

    private void DrawNoSignal(DrawingHandleScreen handle, UIBox2 controlSize, bool active)
    {
        if (!active || _timing.CurTime.TotalSeconds % 2 > 1)
            return;

        var signalText = Error switch
        {
            RemoteControlConsoleError.NotConnected => "NO SIGNAL",
            RemoteControlConsoleError.NoPowerTurret => "NO POWER",
            RemoteControlConsoleError.TurretDestroyed => "SIGNAL LOST",
            _ => string.Empty
        };
        var dimensions = handle.GetDimensions(_font, signalText, 1f);
        var center = controlSize.Center;
        handle.DrawString(_font, center - dimensions / 2f, signalText, Color.Green);
    }


    public void UpdateState(NavInterfaceState scc)
    {
        RadarScreen.UpdateState(scc);
    }
}
