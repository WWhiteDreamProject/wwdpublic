    private void UpdateControlVisibility()
    {
        if (_error != RemoteControlConsoleError.None)
        {
            var error = _error switch
            {
                RemoteControlConsoleError.NotConnected => "NO SIGNAL",
                RemoteControlConsoleError.NoPowerTurret => "NO POWER",
                RemoteControlConsoleError.TurretDestroyed => "SIGNAL LOST",
                _ => "UNKNOWN ERROR\nCONTACT SYSTEM ADMINISTRATOR",
            };
            ShowError(error);
            return;
        }

        switch (AllowedModes)
        {
            case RemoteControlVisualMode.None:
                ShowError("SENSORS FAILURE\nCONTACT SYSTEM ADMINISTRATOR");
                EnableRadarButton.Disabled = true;
                EnableCameraButton.Disabled = true;
                return;
            case RemoteControlVisualMode.Radar:
                UseRadar();
                DisplayTypeHUDLabel.Text = "RADAR ONLY";
                DisplayTypeHUDLabel.FontColorOverride = Color.Yellow;
                EnableRadarButton.Disabled = true;
                EnableCameraButton.Disabled = true;
                return;
            case RemoteControlVisualMode.Camera:
                UseCamera();
                DisplayTypeHUDLabel.Text = "VISUAL ONLY";
                DisplayTypeHUDLabel.FontColorOverride = Color.Yellow;
                EnableRadarButton.Disabled = true;
                EnableCameraButton.Disabled = true;
                return;
        }

        EnableRadarButton.Disabled = false;
        EnableCameraButton.Disabled = false;

        if (EnableCameraButton.Pressed)
        {
            if (_turretEye is null) // still handling invalid settings just in case
            {
                ShowError("NO CAMERA");
                return;
            }

            UseCamera();
            DisplayTypeHUDLabel.Text = "RADAR";
            DisplayTypeHUDLabel.FontColorOverride = Color.AntiqueWhite;
        }
        else
        {
            UseRadar();
            DisplayTypeHUDLabel.Text = "VISUAL";
            DisplayTypeHUDLabel.FontColorOverride = Color.AntiqueWhite;
        }
    }

    private void ShowError(string err)
    {
        ErrorLabel.Text = err;
        RadarScreen.Visible = false;
        CameraScreen.Visible = false;
        StaticBackground.Visible = true;
    }

    private void UseCamera()
    {
        StaticBackground.Visible = false;
        RadarScreen.Visible = false;
        CameraScreen.Visible = true;
        if (RadarScreen.Children.Contains(HUDHolder))
        {
            RadarScreen.RemoveChild(HUDHolder);
            CameraScreen.AddChild(HUDHolder);
        }
    }

    private void UseRadar()
    {
        StaticBackground.Visible = false;
        RadarScreen.Visible = true;
        CameraScreen.Visible = false;
        if (CameraScreen.Children.Contains(HUDHolder))
        {
            CameraScreen.RemoveChild(HUDHolder);
            RadarScreen.AddChild(HUDHolder);
        }
    }

    public void UpdateState(NavInterfaceState scc, IEye? eye, RemoteControlVisualMode mode, RemoteControlConsoleError error)
    {
        RadarScreen.UpdateState(scc);
        _turretEye = eye;
        CameraScreen.Eye = _turretEye;
        _error = error;
        AllowedModes = mode;
        UpdateControlVisibility();
    }
}

public sealed class MouseTracker : Control
{
    public Vector2? MousePosition { get; private set; }
    public bool LeftButtonDown { get; private set; }
    public bool RightButtonDown { get; private set; }
    public event Action<bool>? OnMouseLeftClick;
    public event Action<bool>? OnMouseRightClick;
    public event Action<Vector2?>? OnMouseMove;
    public event Action<int>? OnMouseWheel;
    protected override void MouseMove(GUIMouseMoveEventArgs args)
    {
        base.MouseMove(args);
        if (MousePosition != args.RelativePosition)
        {
            MousePosition = args.RelativePosition;
            OnMouseMove?.Invoke(MousePosition);
        }
    }

    protected override void MouseExited()
    {
        base.MouseExited();
        if (MousePosition != null)
        {
            MousePosition = null;
            OnMouseMove?.Invoke(null);
        }

        if (LeftButtonDown)
        {
            LeftButtonDown = false;
            OnMouseLeftClick?.Invoke(false);
        }

        if (RightButtonDown)
        {
            RightButtonDown = false;
            OnMouseRightClick?.Invoke(false);
        }
    }

    protected override void MouseWheel(GUIMouseWheelEventArgs args)
    {
        base.MouseWheel(args);
        OnMouseWheel?.Invoke((int) MathF.Ceiling(args.Delta.Y));
    }
    protected override void KeyBindDown(GUIBoundKeyEventArgs args)
    {
        base.KeyBindDown(args);
        if (args.Function == EngineKeyFunctions.UIClick)
        {
            LeftButtonDown = true;
            OnMouseLeftClick?.Invoke(LeftButtonDown);
            return;
        }
        else if (args.Function == EngineKeyFunctions.UIRightClick)
        {
            RightButtonDown = true;
            OnMouseRightClick?.Invoke(RightButtonDown);
            return;
        }
    }

    protected override void KeyBindUp(GUIBoundKeyEventArgs args)
    {
        base.KeyBindUp(args);
        if (args.Function == EngineKeyFunctions.UIClick)
        {
            LeftButtonDown = false;
            OnMouseLeftClick?.Invoke(LeftButtonDown);
            return;
        }
        else if (args.Function == EngineKeyFunctions.UIRightClick)
        {
            RightButtonDown = false;
            OnMouseRightClick?.Invoke(RightButtonDown);
            return;
        }
    }

    protected override Vector2 MeasureOverride(Vector2 availableSize) => availableSize;
}